#include <stdio.h>
#include <stdlib.h>

/** \brief Esto es la funcion para sumar.
*
* \param float numeroUno es el primer numero ingresado.
* \param float numeroDos es el segundo numero ingresado.
* \return retorna el resultado de la suma de ambos numeros
*
*/
float sumar (float numeroUno, float numeroDos);

/** \brief Esta es la funcion para restar los numeros.
 *
* \param float numeroUno es el primer numero ingresado.
* \param float numeroDos es el segundo numero ingresado.
 * \return retorna la resta de ambos numeros.
 *
 */
float restar (float numeroUno, float numeroDos);

/** \brief Funcion para multiplicar ambos numeros
 *
* \param float numeroUno es el primer numero ingresado.
* \param float numeroDos es el segundo numero ingresado.
 * \return
 *
 */
float multiplicar (float numeroUno, float numeroDos);

/** \brief Funcion para dividir los numeros.
 *
* \param float numeroUno es el primer numero ingresado.
* \param float numeroDos es el segundo numero ingresado.
 * \return Retorna la division de ambos numeros.
 *
 */
float dividir (float numeroUno, float numeroDos);

/** \brief Funcion que realiza el factorial del numero ingresado.
 *
 * \param float representa el numero al que se le es llamado.
 * \param
 * \return retorna el factorial del numero ingresado.
 *
 */
float factorial (float);
